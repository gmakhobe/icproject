      <h2 class="main-content-heading text-color-black">WHAT ARE SMALL CAP STOCKS</h2>
      <p class="aboutus-paragraph">Small
        cap stocks are stocks of companies with a small market capitalization (the cap in small cap ). We say that
        a stock is a small cap stock when the total value of all of the company’s shares outstanding meaning the
        shares held by all shareholders, including company insiders falls roughly between $300 million and $2
        billion.
        Small
        cap companies are often young companies: They often have lots of growth potential, but they may also
        have less stability and market share than larger, established companies.
        That’s exactly how it tends to play out with small
        cap stocks. The risks may be higher versus larger cap stocks,
        but the rewards may also be greater. (Note that the risks can be even greater with micro cap stocks . Unless
        you’re a very experienced investor, it’s best to steer clear of stocks with market caps under $300 million.)
        Since 2000, small
        cap stocks have outperformed large cap stocks over the long term by 2% per year. But the
        story is often different over shorter periods (think three to five years or so), because small cap stocks tend to
        be more volatile than larger companies, with bigger ups and downs in their prices. Over longer periods, those
        ups and downs will seem to smooth out.
        In 2020, for example, small
        caps have vastly underperformed their large cap counterparts. In the first half of
        the year, the small cap Russell 2000 index lost 13.6%, while the large cap focused S&P 500 gave up only 4%.
        Large cap companies are more likely to be profitable, have ample cash on their balance sheets, and have better
        access to capital, making them less risky in a crisis like the COVID 19 pandemic. Investors feel safer with large
        cap stocks and many likely moved money out of small caps and into large caps during the pandemic. Of course,
        in a recovery you would expect small cap stocks to outperform, as these stocks have greater growth potential.</p>
